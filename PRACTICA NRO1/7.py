NumeroEntero=int(input("Ingrese el número: "))

if NumeroEntero % 2 == 0:
    print("El número es par")
else:
    print("El número es impar") 


#Cuando se trata de división de números enteros en programación, el operador % (módulo) es muy útil. Este operador devuelve el residuo de la división entre dos números. Por lo tanto, si tenemos un número entero n y lo dividimos por 2, si el residuo es 0, entonces n es un número par. Si el residuo es diferente de 0, entonces n es un número impar.